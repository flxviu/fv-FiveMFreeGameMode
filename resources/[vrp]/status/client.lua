function LoadShowerAnimations(isFemale)
    if isFemale then
        LoadDict("mp_safehouseshower@female@")
        LoadDict("anim@mp_yacht@shower@female@")
    else
        LoadDict("amb@world_human_bum_wash@male@high@idle_a")
        LoadDict("anim@mp_yacht@shower@male@")
    end
end

function StartShowerParticles(playerPed)
    local asset = "scr_carwash"
    RequestNamedPtfxAsset(asset)
    
    while not HasNamedPtfxAssetLoaded(asset) do
        Citizen.Wait(100)
    end

    for i = 1, 5 do
        UseParticleFxAssetNextCall(asset)
        local pos = GetEntityCoords(playerPed)
        local particle = StartParticleFxLoopedAtCoord('ent_amb_car_wash_jet', pos.x, pos.y, pos.z + 1.3, -180.0, 0.0, 0.0, 1.0, false, false, false, false)
        table.insert(sparticleEffects, particle)
        Citizen.Wait(50)
    end
end

function StopShowerParticles()
    for _, particle in pairs(sparticleEffects) do
        StopParticleFxLooped(particle, true)
        RemoveParticleFx(particle, false)
    end
    sparticleEffects = {}
end

function PlayShowerAnimation(playerPed, isFemale)
    if isFemale then
        TaskPlayAnim(playerPed, "mp_safehouseshower@female@", "shower_enter_into_idle", 8.0, -8.0, 5000, 0, 0.0, false, false, false)
        Citizen.Wait(5000)
        TaskPlayAnim(playerPed, "anim@mp_yacht@shower@female@", "shower_idle_a", 8.0, -8.0, 5000, 0, 0.0, false, false, false)
    else
        TaskPlayAnim(playerPed, "anim@mp_yacht@shower@male@", "male_shower_idle_d", 8.0, -8.0, 5000, 0, 0.0, false, false, false)
        Citizen.Wait(5000)
        TaskPlayAnim(playerPed, "anim@mp_yacht@shower@male@", "male_shower_idle_a", 8.0, -8.0, 5000, 0, 0.0, false, false, false)
    end
end

